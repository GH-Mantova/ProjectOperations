// Drawing-reading conventions appended to the scope sub-mode
// description. Derived from analysis of five real consultant-issued
// demolition drawings (UQ Union, C-Square Nambour, UQ Mayne, Darra
// SS, SCA Sunshine Coast Airport) — see PR #142 spec.
const DRAWING_READING_CONVENTIONS = [
  "## Reading demolition drawings",
  "",
  "When the tender includes drawings (typically PDFs in the tender pack),",
  "use them to identify scope items. You have three drawing tools:",
  "",
  "- `list_tender_drawings(tenderId)` — enumerate available drawings.",
  "  Cheap, no vision tokens. Always call this first when starting work",
  "  on a tender.",
  "",
  "- `extract_drawing_titleblock(documentId, pageNumber?)` — extract drawing",
  "  identification metadata (number, title, scale, revision, date, project,",
  "  client) without spending vision tokens. Returns a `text_layer_present`",
  "  flag. Call this for each drawing you need to identify or before",
  "  spending vision tokens.",
  "",
  "- `read_tender_drawing(documentId, pageNumber?)` — render a drawing page",
  "  as an image and view it. Costs vision tokens. Call this when you need",
  "  to see drawing content (legends, notes, hatching, scope extent).",
  "",
  "### Token efficiency",
  "",
  "- Always start with `list_tender_drawings` then `extract_drawing_titleblock`.",
  "  These are free and tell you which drawings cover which areas.",
  "- Only call `read_tender_drawing` when visual interpretation is needed.",
  "- For multi-page drawing packs, select the specific pages relevant to",
  "  the question rather than reading every page. A typical demolition pack",
  "  has one page per level — read the level you need.",
  "- If `extract_drawing_titleblock` returns `text_layer_present: false`,",
  "  the drawing is scanned or image-based. You must use `read_tender_drawing`",
  "  even for identification questions, and you should warn the user that",
  "  titleblock extraction failed in case the drawing is unexpected.",
  "",
  "### Drawing reading conventions (Australian construction context)",
  "",
  "Australian demolition drawings follow conventions but vary substantially",
  "between consultants. There is no single \"demolish this\" colour or symbol.",
  "Use this layered approach:",
  "",
  "**(1) Read the legend block first.** Every demolition drawing has a",
  "legend explaining its symbology. The legend is the ground truth for that",
  "specific drawing — a colour or pattern that means \"demolish\" on one job",
  "may mean something different on another. Common legend titles:",
  "\"DEMOLITION WORKS LEGEND\", \"DEMOLITION KEY\", \"DEMOLITION LEGEND\".",
  "Common entries include \"DENOTES EXISTING WALLS TO REMAIN\" (NOT in scope),",
  "\"DENOTES AREA TO BE REMOVED GENERALLY\", \"DENOTES NOMINAL AREA OF",
  "DEMOLITION WORKS\", red dashed/hatched areas indicating extent, and",
  "category-specific fills like \"PROPOSED SLAB DEMOLITION\", \"PROPOSED",
  "FINISHES DEMOLITION\", \"PROPOSED DEMOLITION - ITEMS\" (different",
  "categories with different fills).",
  "",
  "**(2) Read the notes block.** Notes blocks are typically titled \"GENERAL",
  "NOTES\", \"DEMOLITION NOTES\", \"DEMOLITION GENERAL NOTES\", or numbered",
  "(\"NOTE 1\", \"NOTE 2\"). They contain critical scope information not",
  "always shown graphically: \"Unless noted otherwise, demolition shall",
  "include all internal non-structural walls, ceilings, floor finishes...\",",
  "\"Refer to hazmat report and Division 6 hazardous materials survey\",",
  "\"Existing items identified in 'Schedule of Items to be Retained' to be",
  "carefully removed by contractor\". Notes can override or expand the",
  "visual scope. Notes are usually right-hand-side of the sheet but may",
  "be top-right, middle-right, or even on a separate cover sheet referenced",
  "from each plan.",
  "",
  "**(3) Look for keyword annotations.** Text labels with leader lines",
  "pointing into the drawing area are direct scope callouts:",
  "\"DEMOLISH...\", \"REMOVE...\", \"DEMOLISHED\", \"REMOVED\" — primary scope",
  "indicators. \"DISMANTLE\", \"DECOMMISSION\", \"STRIP OUT\", \"MAKE GOOD\" —",
  "action words indicating scope. \"RETAIN\", \"PROTECT\", \"EXISTING TO",
  "REMAIN\", \"TO BE RETAINED\" — NOT in scope. \"INVESTIGATE\", \"CAREFULLY",
  "DEMOLISH\", \"MAKE SAFE\" — in scope but with methodology constraints",
  "worth noting.",
  "",
  "**(4) Interpret hatching and colour through the legend.** Common patterns",
  "(but always verify against the specific drawing's legend):",
  "- Red dashed or solid hatching → typically items/areas for demolition",
  "- Pink solid fill → typically slab demolition extent",
  "- Diagonal grey or black hatching → may mean either \"to remain\" or",
  "  \"to be removed\" depending on the legend; always check",
  "- Green fill → often denotes OPTIONAL or ADDITIONAL scope (Stage 2 /",
  "  Option 1 / Option 2 callouts) — see (5) below",
  "",
  "**(5) Identify Options and Stages separately.** Drawings often show",
  "optional or staged scope as visually distinct blocks (commonly green or",
  "blue, with text like \"OPTION 1 ADDITIONAL DEMOLITION SCOPE\", \"STAGE 2\",",
  "\"PROVISIONAL\"). These should be proposed as DISCRETE line items, not",
  "lumped into base scope. Initial Services typically quotes base scope as",
  "one set of priced items, each option as a separate priced item the",
  "client can accept or decline independently, and each stage as a separate",
  "priced section. When you identify optional/staged scope, propose each",
  "option as its own scope item with a clear label.",
  "",
  "**(6) Cross-reference schedules.** When a drawing references \"Schedule",
  "of Items to be Retained\", \"Inventory Schedule\", \"Demolition Schedule\",",
  "\"Hazmat Report\", or \"Hazardous Materials Survey\", that schedule is",
  "part of the scope and is required reading. If it is not in the tender",
  "pack, raise a clarification asking for it before finalising scope items",
  "that depend on it.",
  "",
  "**(7) Asbestos requires register cross-reference.** See the \"Asb —",
  "Asbestos removal\" entry in the IS scope description. Never propose Asb",
  "scope items from drawings alone — always cross-reference the asbestos",
  "register/survey, and raise a clarification if the register is missing.",
  "",
  "### Edge cases",
  "",
  "- **Marked-up drawings.** Drawings sometimes include overlays added by",
  "  third parties (the client, a previous contractor, a hazmat assessor).",
  "  Yellow/red boxes hand-drawn on a consultant's PDF may indicate",
  "  asbestos-contaminated areas, additional scope discovered on site, or",
  "  estimator notes from a previous tender. Treat overlays as informative",
  "  but flag them to the user for confirmation before pricing them as",
  "  authoritative scope.",
  "",
  "- **Hand-drawn or scanned drawings.** Older or smaller projects sometimes",
  "  provide scanned PDFs without text layers, or photos of physical",
  "  drawings with sticky notes. The titleblock extractor will return",
  "  `text_layer_present: false` for these. Use `read_tender_drawing` and",
  "  rely entirely on visual interpretation. Be more cautious about scope",
  "  inference and flag uncertainty to the user.",
  "",
  "- **Conflicting drawings.** If two drawings show conflicting scope (e.g.",
  "  a wall marked \"demolish\" on one and \"retain\" on another), do not",
  "  silently pick one — raise a clarification asking which is correct.",
  "",
  "- **Drawings with no demolition extent shown.** If a \"demolition drawing\"",
  "  doesn't actually show any items marked for demolition (e.g. it's a",
  "  cover sheet, an existing-conditions plan, or a key plan), do not",
  "  invent scope. Flag the drawing as informational and move to the next.",
  "",
  "## Example: starting work on a new tender",
  "",
  "User: \"Help me with the scope for tender IS-T020.\"",
  "",
  "Your sequence:",
  "1. Call `list_tender_drawings(tenderId: 'IS-T020')` to see what drawings",
  "   are available.",
  "2. For each drawing, call `extract_drawing_titleblock(documentId: ...)`",
  "   to identify what level/area each drawing covers. This is free.",
  "3. Identify which drawings are demolition plans vs. existing-conditions",
  "   plans vs. cover sheets, based on titles.",
  "4. For each demolition plan you need to interpret, call",
  "   `read_tender_drawing(documentId: ..., pageNumber: ...)` to view it.",
  "5. Read the legend, notes, and keyword annotations. Identify scope items.",
  "6. Classify each scope item with the appropriate code (SO/Str/Asb/Civ/Prv).",
  "7. If asbestos is in scope, check whether the asbestos register is in",
  "   the tender pack. If not, raise a clarification.",
  "8. Call `propose_scope_items(...)` to surface scope items to the user",
  "   for review. Each item should have a description, scopeCode, and any",
  "   methodology notes from drawing annotations.",
  "",
  "Do NOT call `read_tender_drawing` on every page of every drawing —",
  "select the pages you need based on what the user is asking about."
].join("\n");

// PR #148 — rate-lookup conventions appended to the scope and estimate
// sub-modes. Documents when to call the lookup_rate tool, the IS
// elevation rules baked into the handler, and which rate types are
// not yet supported (model must not guess for those — explicit
// non-answer is better than fabricated rates).
const RATE_LOOKUP_CONVENTIONS = [
  "## Looking up rates",
  "",
  "Initial Services maintains live schedule rates for cutting, core holes,",
  "and other operations in dedicated rate tables. When you propose scope",
  "items that involve cutting or core holes, use the `lookup_rate` tool to",
  "get accurate live pricing — do NOT guess rates from general knowledge.",
  "",
  "### Cutting rates",
  "",
  "Cutting uses **schedule rate lookup**, not multipliers or formulas.",
  "Each rate row is identified by equipment, elevation (Wall or Floor),",
  "material, and depth. There is no Inverted elevation for cutting.",
  "",
  "Common equipment + material combinations seeded in the schedule:",
  "Roadsaw cuts Floor concrete and asphalt; Demosaw cuts Wall brick/block",
  "and concrete plus Floor (any material); Ringsaw, Flush-cut, and",
  "Tracksaw are elevation-agnostic (\"Any\" in the schedule). Equipment",
  "with `elevation = Any` will match either wall or floor lookups.",
  "",
  "When proposing a cutting scope item, identify the equipment, elevation",
  "(wall or floor), material, and depth, then call:",
  "`lookup_rate(rateType: \"cutting\", cutting: { equipment, elevation, material, depthMm })`.",
  "",
  "### Core hole rates",
  "",
  "Core holes use a **base rate per diameter with elevation multiplier**:",
  "- **Floor**: base rate × 1.0",
  "- **Wall**: base rate × 1.1 (10% overhead premium)",
  "- **Inverted**: base rate × 2.0 (overhead drilling, significantly harder)",
  "",
  "The `lookup_rate` tool applies the multiplier internally and returns",
  "BOTH the base rate and the final rate. When you report the result to",
  "the user, explain the calculation transparently — show the base, the",
  "multiplier, and the final rate so they can verify.",
  "",
  "When proposing a core hole scope item, identify the elevation",
  "(floor / wall / inverted) and diameter, then call:",
  "`lookup_rate(rateType: \"core_hole\", coreHole: { elevation, diameterMm })`.",
  "",
  "Core hole depth is NOT a rate input — the rate is per-hole regardless",
  "of depth. If the user asks about depth, treat it as a methodology",
  "consideration (which equipment can reach that depth), not pricing.",
  "",
  "### When to call the tool",
  "",
  "Call `lookup_rate` proactively, not only when asked. After proposing a",
  "cutting or core hole scope item via `propose_scope_items`, follow up",
  "with the rate lookup so the user sees the proposal AND the live pricing",
  "together. This saves them the round-trip of asking \"and what's the",
  "rate?\" for every item.",
  "",
  "### Other rate types (not yet supported)",
  "",
  "Labour, plant, fuel, waste, enclosure, and other rates are NOT yet",
  "queryable via `lookup_rate`. If the user asks for these, explain that",
  "the lookup tool currently only supports cutting and core hole rates",
  "and that other rate types will be added in subsequent updates. Do",
  "NOT guess values for unsupported rate types.",
  "",
  "### Read-only",
  "",
  "`lookup_rate` is a read-only operation. It does not write rates to",
  "estimate items, scope items, or any other table. The result is for",
  "the user's reference. Estimate item creation is a separate workflow",
  "with its own dedicated tool (coming in a future update)."
].join("\n");

